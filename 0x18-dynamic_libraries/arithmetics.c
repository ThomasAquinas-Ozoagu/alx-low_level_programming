/**
 * add - sums two integers
 * @a: The first integer
 *
 * @b: The second integer
 *
 * Return: The sum of both integers
 */
int add(int a, int b)
{
	return (a + b);
}

/**
 * sub - subtracts one integer from the other
 * @a: The first integer
 *
 * @b: The second integer
 *
 * Return: The difference between both integers
 */
int sub(int a, int b)
{
	return (a - b);
}


/**
 * mul - multiplies two integers
 * @a: The first integer
 *
 * @b: The second integer
 *
 * Return: The product of both integers
 */
int mul(int a, int b)
{
	return (a * b);
}


/**
 * div - divides two integers
 * @a: The first integer
 *
 * @b: The second integer
 *
 * Return: The quotient when one integer divides the other
 */
int div(int a, int b)
{
	return (a / b);
}


/**
 * mod - finds the remainder after one integer divides another
 * @a: The first integer
 *
 * @b: The second integer
 *
 * Return: Returns the remainder
 */
int mod(int a, int b)
{
	return (a / b);
}
